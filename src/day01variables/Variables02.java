package day01variables;

public class Variables02 {

	public static void main(String[] args) {
		char letter1 = 'a'; // 97 value
		char letter2 = 'b'; // 98 value

		System.out.println(letter1 + letter2); // syso ctrl+space short way
		System.out.println(letter1); // a
		System.out.println(letter2); // b

		// The following method is used to  write ab.
		System.out.print(letter1);
		System.out.println(letter2);

		//how to find a character's number value without using table ASCII
		int aCharValue = 'a';
		System.out.println(aCharValue);
		// Create a variable with int data type and assign it.

		// ! % * What are the number values?
		int exclamationCharValue = '!';
		int percentCharValue = '%';
		int asteriksCharValue = '*';

		System.out.println(exclamationCharValue);
		System.out.println(percentCharValue);
		System.out.println(asteriksCharValue);

	}

}
