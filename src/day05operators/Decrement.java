package day05operators;

public class Decrement {

	public static void main(String[] args) {
		// create a variable in int data type
		// reduce the value of this variab by 1 each time using 3 methods.
		
		
		// Long way
		int num1 = 12;
		num1 = num1-1;
		System.out.println(num1);

		// Short way
		
		num1 -=1;
		System.out.println(num1);
		
		//Shortest way
		
		num1--;
		System.out.println(num1);
		
	}

}
