package day05operators;

public class IncrementDecrement {

	public static void main(String[] args) {
		// create a variable in int data type.
		// using multiplication and division of the value of this variability
		// increase and decrease.
		
		int num1 = 12;
		
		// increment by multiplying with the long way
		
		num1 = num1*3;
		System.out.println(num1); // 36
		
		// Short way
		
		num1 *=3;
		System.out.println(num1); //108
		
		// decrement by division with the long way
		
		num1 = num1 /9;
		System.out.println(num1); //12
		
		// Short way
		num1 /=3;
		System.out.println(num1); //4
		
		
		

	}

}
