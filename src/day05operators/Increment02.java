package day05operators;

public class Increment02 {

	public static void main(String[] args) {
		// Create two variables in int data type
		// increase the first 3 and the second 7.
		
		
		// Increment 2 ==> Short Way
		int num1 = 12;
		num1 +=3;
		System.out.println(num1);
		
		// There will be no space between + and =
		
		int num2 = 20;
		num2 +=7;
		System.out.println(num2);

	}

}
