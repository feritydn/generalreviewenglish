package day05operators;

public class Increment03 {

	public static void main(String[] args) {
		// create two variables in int data type.
		// Increase these variables by using the shortest path 1.

		// The shortest path is used to increase only 1.

		int num1 = 21;
		num1++;

		System.out.println(num1);

		// if you type the + symbol twice after the variable name
		// the value of that variable increases by 1.

		int num2 = 35;
		num2++;
		System.out.println(num2);

	}

}
